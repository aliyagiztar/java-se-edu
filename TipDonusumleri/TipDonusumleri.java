package TipDonusumleri;

public class TipDonusumleri {
    public static void main(String[] args) {
        float sayi1 = 10.5f; // Float türünde birinci sayı
        float sayi2 = 20.3f; // Float türünde ikinci sayı

        float toplam = sayi1 + sayi2; // Sayıları toplama

        System.out.println("Toplam: " + toplam); // Toplamı yazdırma

    }
    public TipDonusumleri() {
        float sayi1 = 10.5F;
        float sayi2 = 20.3F;
        float toplam = sayi1 + sayi2;
        System.out.println("Toplam: " + toplam);
    }
}
